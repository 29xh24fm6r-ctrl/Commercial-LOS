describe('Phase 106 — NOT_WIRED.email-delivery is absent', () => {
  it('NOT_WIRED contains no entry with id "email-delivery"', () => {
    const entry = NOT_WIRED.find((n) => n.id === 'email-delivery');
    expect(
      entry,
      'NOT_WIRED.email-delivery should have been retired in Phase 105 ' +
        'when the borrower-update LIVE send shipped. Its presence now would ' +
        'falsely claim borrower-update email is still local-only.',
    ).toBeUndefined();
  });

  it('NOT_WIRED contains no Outlook-connector-shaped row at all (Phase 104 + 105 closed every one)', () => {
    const ids = new Set(NOT_WIRED.map((n) => n.id));
    expect(ids.has('email-delivery')).toBe(false);
    expect(ids.has('outlook-connector-live-send')).toBe(false);
    expect(ids.has('outlook-send')).toBe(false);
    expect(ids.has('borrower-update-email')).toBe(false);
    expect(ids.has('document-request-email')).toBe(false);
  });

  it('no NOT_WIRED reason text describes the LIVE Outlook send as missing', () => {
    // Phase 106 honesty: the inventory's free-text reason fields must
    // not say "Outlook is not registered" or similar after the swap.
    for (const n of NOT_WIRED) {
      expect(
        n.reason,
        `NOT_WIRED.${n.id} reason should not claim the Outlook ` +
          'connector is unregistered — Phase 104 / 105 closed that.',
      ).not.toMatch(/outlook\s+connector\s+is\s+(?:not\s+yet\s+)?registered/i);
      expect(
        n.reason,
        `NOT_WIRED.${n.id} reason should not claim Outlook LIVE is missing.`,
      ).not.toMatch(/outlook\s+live\s+(?:is|remains)\s+(?:not\s+wired|missing|unwired)/i);
    }
  });
});

// ---------------------------------------------------------------------------
// Invariant 2 — ReleaseReadinessGate doesn't surface email-delivery
//
// The platformInventory data drives the gate's Capability Inventory
// section. With email-delivery retired, the row must not appear.
// We assert this at the data layer (the gate is a pure render of
// inventory + governance signals; the data-layer test is sufficient
// and avoids re-rendering the heavy admin component here).
