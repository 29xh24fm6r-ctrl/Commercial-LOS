// ---------------------------------------------------------------------------
// Invariant 6 — no email payload expansion
//
// The two Outlook governed writes use ONLY: To, Subject, Body,
// Importance. Nothing else. The adapter's static source must not
// reference Attachments / Cc / Bcc / From / ReplyTo / Sensitivity /
// shared mailbox / Graph / calendar / inbound vocabulary in a way
// that would imply expansion.
// ---------------------------------------------------------------------------

describe('Phase 106 — no email payload expansion (To / Subject / Body / Importance only)', () => {
  const ADAPTER_FILE = 'src/deals/emailDelivery/outlookEmailAdapters.ts';
  const ACTION_FILES: readonly string[] = [
    'src/deals/sendDocumentRequestEmail.ts',
    'src/deals/sendBorrowerUpdateEmail.ts',
  ];

  it('the LIVE adapter sets EXACTLY the four expected ClientSendHtmlMessage fields', () => {
    const src = readSource(ADAPTER_FILE);
    // The payload literal in the LIVE adapter is the canonical site.
    // Pin the four field names appear; pin the forbidden field names
    // do not appear in the adapter source at all.
    expect(src).toMatch(/\bTo\s*:\s*input\.recipient\b/);
    expect(src).toMatch(/\bSubject\s*:\s*input\.subject\b/);
    expect(src).toMatch(/\bBody\s*:\s*input\.body\b/);
    expect(src).toMatch(/\bImportance\s*:\s*['"]Normal['"]/);
  });

  it('the LIVE adapter source does NOT reference forbidden ClientSendHtmlMessage fields', () => {
    const src = readSource(ADAPTER_FILE);
    const forbiddenFieldNames = [
      'Attachments',
      'Cc',
      'Bcc',
      'From',
      'ReplyTo',
      'Sensitivity',
    ] as const;
    for (const name of forbiddenFieldNames) {
      // We forbid the field name as a property key (followed by ':')
      // so we don't false-positive on words like "from" in prose.
      // Block-comment text is stripped to keep doc-prose honest
      // ("From: borrower@...") from tripping the pin.
      const stripped = src.replace(/\/\*[\s\S]*?\*\//g, ' ').replace(/(^|[^:])\/\/[^\n]*/g, '$1');
      const pattern = new RegExp(`\\b${name}\\b\\s*:`);
      expect(
        pattern.test(stripped),
        `Adapter source must not set a ${name} field on the payload`,
      ).toBe(false);
    }
  });

  it('the LIVE adapter source does NOT contain shared-mailbox / Graph / calendar / inbound vocabulary in code', () => {
    const src = readSource(ADAPTER_FILE);
    const stripped = src.replace(/\/\*[\s\S]*?\*\//g, ' ').replace(/(^|[^:])\/\/[^\n]*/g, '$1');
    // Forbidden code-level vocabulary. Each pattern targets a likely
    // API surface, not banker-facing prose.
    const forbiddenCodePatterns: readonly RegExp[] = [
      /\bsharedMailbox\b/i,
      /\bSendEmailFromShared/i,
      /\bmicrosoftGraph\b/i,
      /\bGraphClient\b/,
      /\bCalendar(?:Events?|Event)\b/,
      /\bGetEmails(?:V[0-9]+)?\b/,
      /\bOnNewEmail\b/,
      /\bGetMailTips\b/,
      /\bSubscribeMail(?:box)?\b/i,
    ];
    for (const pat of forbiddenCodePatterns) {
      expect(
        pat.test(stripped),
        `Adapter source must not reference ${pat} in code (shared-mailbox / Graph / calendar / inbound surface).`,
      ).toBe(false);
    }
  });

  it('each governed-write action passes ONLY recipient / subject / body / correlationId to the adapter', () => {
    // The adapter.send() call site in each action must reference
    // exactly these four fields. Anything else (e.g. attachments,
    // cc, bcc, from) would be a Phase 106 violation.
    for (const file of ACTION_FILES) {
      const src = readSource(file);
      // The adapter.send call sits inside the action's body. Pin the
      // four expected argument-object keys with a strict regex.
      expect(
        src,
        `${file} must call adapter.send with { recipient, subject, body, correlationId }`,
      ).toMatch(
        /adapter\.send\s*\(\s*\{\s*recipient\s*,\s*subject\s*:\s*input\.subject\s*,\s*body\s*:\s*input\.body\s*,\s*correlationId\s*,\s*\}\s*\)/,
      );
    }
  });

  it('neither action sets attachments / Cc / Bcc / From / ReplyTo / Sensitivity on its own payload', () => {
    for (const file of ACTION_FILES) {
      const src = readSource(file);
      const stripped = src
        .replace(/\/\*[\s\S]*?\*\//g, ' ')
        .replace(/(^|[^:])\/\/[^\n]*/g, '$1');
      for (const name of [
        'Attachments',
        'Cc',
        'Bcc',
        'ReplyTo',
        'Sensitivity',
      ] as const) {
        const pattern = new RegExp(`\\b${name}\\b\\s*:`);
        expect(
          pattern.test(stripped),
          `${file} must not set a ${name} field anywhere in its code.`,
        ).toBe(false);
      }
    }
  });

  it('the Outlook adapter does not claim delivery — "Outlook accepted" copy only', () => {
    // Phase 106 wording requirement: the LIVE adapter's user-facing
    // copy uses "accepted" semantics, never claims borrower delivery.
    // Match against the action layers (which produce the user-visible
    // timeline summary) and the adapter (which carries failure-mode
    // strings).
    for (const file of ['src/deals/sendDocumentRequestEmail.ts', 'src/deals/sendBorrowerUpdateEmail.ts']) {
      const src = readSource(file);
      // The success branch of describeSendOutcome MUST say "Outlook
      // accepted" (or equivalent acceptance wording) for the LIVE
      // path.
      expect(src).toMatch(/Outlook\s+(?:accepted|send\s+accepted|accepted\s+borrower\s+update|accepted\s+document\s+request)/i);
      // It MUST NOT positively claim delivery anywhere in the file.
      // Code-level check: forbid "delivered" / "was sent" / "has been
      // sent" / "email sent" as a positive substring.
      const stripped = src
        .replace(/\/\*[\s\S]*?\*\//g, ' ')
        .replace(/(^|[^:])\/\/[^\n]*/g, '$1');
      expect(
        /\bdelivered\b/i.test(stripped),
        `${file} must not contain the word "delivered" in code (claims of delivery are forbidden).`,
      ).toBe(false);
      expect(
        /\bemail\s+(?:was|has\s+been)\s+(?:sent|delivered)\b/i.test(stripped),
        `${file} must not claim the email "was sent" or "has been sent"/"delivered" (Outlook accepted only).`,
      ).toBe(false);
    }
  });
});
