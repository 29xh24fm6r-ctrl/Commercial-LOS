// ---------------------------------------------------------------------------
// Invariant 3 — Outlook LIVE send is represented ONLY for two writes
//
// Two static-source signals:
//   (a) GOVERNED_WRITES contains exactly two ids that consume the
//       Outlook LIVE adapter, and they are the expected pair.
//   (b) exactly one production file imports Office365OutlookService,
//       and exactly one production file calls SendEmailV2 — the
//       adapter at src/deals/emailDelivery/outlookEmailAdapters.ts.
//   (c) getEmailAdapter() is consumed by EXACTLY the two action files.
// ---------------------------------------------------------------------------

describe('Phase 106 — Outlook LIVE send availability is bounded to two governed writes', () => {
  it('GOVERNED_WRITES contains both expected email send ids', () => {
    const ids = new Set(GOVERNED_WRITES.map((w) => w.id));
    for (const expected of LIVE_EMAIL_WRITE_IDS) {
      expect(
        ids.has(expected),
        `expected GOVERNED_WRITES to contain ${expected}`,
      ).toBe(true);
    }
  });

  it('GOVERNED_WRITES contains no other Outlook-send write surface', () => {
    // Any new id ending in "-email" that is not one of the two
    // expected writes would represent an unauthorized expansion.
    const otherEmailIds = GOVERNED_WRITES.filter((w) => w.id.endsWith('-email'))
      .map((w) => w.id)
      .filter((id) => !(LIVE_EMAIL_WRITE_IDS as readonly string[]).includes(id));
    expect(
      otherEmailIds,
      `Unexpected email governed writes: ${otherEmailIds.join(', ')}`,
    ).toEqual([]);
  });

  it('exactly one production source file imports Office365OutlookService', () => {
    const matches: string[] = [];
    for (const abs of PRODUCTION_SOURCE_FILES) {
      const rel = relForward(abs);
      if (rel.endsWith('emailLiveReleaseReadiness.test.ts')) continue;
      const src = readFileSync(abs, 'utf8');
      if (/from\s+['"][^'"]*Office365OutlookService['"]/.test(src)) {
        matches.push(rel);
      }
    }
    expect(matches).toEqual([
      'src/deals/emailDelivery/outlookEmailAdapters.ts',
    ]);
  });

  it('exactly one production source file calls Office365OutlookService.SendEmailV2(...) in CODE', () => {
    // Comments stripped — doc-prose in action files legitimately
    // references SendEmailV2; only code-level callsites count.
    const matches: string[] = [];
    for (const abs of PRODUCTION_SOURCE_FILES) {
      const rel = relForward(abs);
      if (rel.endsWith('emailLiveReleaseReadiness.test.ts')) continue;
      const src = stripComments(readFileSync(abs, 'utf8'));
      if (/Office365OutlookService\.SendEmailV2\s*\(/.test(src)) {
        matches.push(rel);
      }
    }
    expect(matches).toEqual([
      'src/deals/emailDelivery/outlookEmailAdapters.ts',
    ]);
  });

  it('no production source file calls any Office365OutlookService method OTHER than SendEmailV2 in CODE', () => {
    // The generated service exports calendar/contact/batch/subscription
    // methods. None of them must be referenced by production source.
    const forbiddenMethodPattern =
      /Office365OutlookService\.(?!SendEmailV2\b)[A-Za-z][A-Za-z0-9_]*\s*\(/g;
    const violations: string[] = [];
    for (const abs of PRODUCTION_SOURCE_FILES) {
      const rel = relForward(abs);
      if (rel.endsWith('emailLiveReleaseReadiness.test.ts')) continue;
      const src = stripComments(readFileSync(abs, 'utf8'));
      const hits = src.match(forbiddenMethodPattern);
      if (hits) {
        for (const h of hits) violations.push(`${rel}: ${h}`);
      }
    }
    expect(violations).toEqual([]);
  });

  it('getEmailAdapter() is consumed by exactly the two governed-write actions + the Phase 109 operator smoke helper', () => {
    // Phase 109 added one new caller — the operator-triggered smoke
    // test helper at src/admin/emailLiveSmokeTest.ts. The smoke
    // helper is NOT a governed write; it's an admin-only diagnostic
    // that reuses the SAME adapter so no new SendEmailV2 callsite is
    // introduced. The Phase 106 "exactly one production file imports
    // Office365OutlookService" + "exactly one production file calls
    // SendEmailV2 in CODE" pins remain unchanged.
    const callers: string[] = [];
    for (const abs of PRODUCTION_SOURCE_FILES) {
      const rel = relForward(abs);
      // The adapter file itself DEFINES the function; only count callers.
      if (rel === 'src/deals/emailDelivery/outlookEmailAdapters.ts') continue;
      if (rel.endsWith('emailLiveReleaseReadiness.test.ts')) continue;
      const src = readFileSync(abs, 'utf8');
      if (/\bgetEmailAdapter\s*\(/.test(src)) callers.push(rel);
    }
    expect(callers.sort()).toEqual(
      [
        'src/admin/emailLiveSmokeTest.ts',
        'src/deals/sendBorrowerUpdateEmail.ts',
        'src/deals/sendDocumentRequestEmail.ts',
      ].sort(),
    );
  });
});

