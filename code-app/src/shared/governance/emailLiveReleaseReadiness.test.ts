/**
 * Phase 106 — Outlook LIVE email mode release-readiness pin.
 *
 * No production code change ships with this phase. This single test
 * file is the entire deliverable beyond the docs. Its purpose is to
 * prove — at CI time — that operating `VITE_EMAIL_MODE=LIVE` is safe
 * for EXACTLY the two banker-triggered governed writes the
 * Phase 104 / Phase 105 swap landed:
 *
 *   - GOVERNED_WRITES.deal-document-request-email   (Phase 61 / 104)
 *   - GOVERNED_WRITES.deal-borrower-update-email    (Phase 105)
 *
 * Six invariants are pinned, each as a top-level describe block. If
 * any of these starts failing, the failure is a structural signal —
 * something has either accidentally expanded the LIVE send surface
 * (new method, new payload field, new caller, new automation
 * pathway) or accidentally regressed the honesty of the inventory.
 * Either is a release-readiness regression for LIVE mode.
 *
 * Sibling files used as the canonical references:
 *   - docs/PHASE_106_EMAIL_MODE_RELEASE_READINESS.md
 *   - docs/PHASE_104_OUTLOOK_LIVE_SEND.md
 *   - docs/PHASE_105_BORROWER_UPDATE_LIVE_SEND.md
 *   - src/deals/emailDelivery/outlookEmailAdapters.ts
 *   - src/deals/sendDocumentRequestEmail.ts
 *   - src/deals/sendBorrowerUpdateEmail.ts
 *   - src/shared/governance/platformInventory.ts
 */

const REPO_ROOT = resolve(__dirname, '..', '..', '..');

function readSource(rel: string): string {
  return readFileSync(resolve(REPO_ROOT, rel), 'utf8');
}

/**
 * Strips block and line comments before scanning. Necessary for the
 * "exactly one production file CALLS SendEmailV2" pin, because docs
 * comments in action files legitimately reference "SendEmailV2(...)"
 * as prose. Code is what matters; prose can describe.
 */
function stripComments(src: string): string {
  return src
    .replace(/\/\*[\s\S]*?\*\//g, ' ')
    .replace(/(^|[^:])\/\/[^\n]*/g, '$1');
}

function relForward(abs: string): string {
  return relative(REPO_ROOT, abs).split(sep).join('/');
}

/**
 * Collects every .ts / .tsx file under a directory recursively,
 * EXCLUDING:
 *   - .test.ts / .test.tsx (test files are allowed to mock connector
 *     services, reference forbidden vocabulary in negation form, etc.)
 *   - src/generated/ (autogenerated SDK — must not be edited and
 *     legitimately exports every Outlook method the connector ships)
 */
function collectProductionSourceFiles(dirAbs: string, out: string[]): void {
  for (const entry of readdirSync(dirAbs)) {
    const abs = resolve(dirAbs, entry);
    const stat = statSync(abs);
    if (stat.isDirectory()) {
      if (relForward(abs).endsWith('src/generated')) continue;
      collectProductionSourceFiles(abs, out);
    } else if (
      stat.isFile() &&
      (entry.endsWith('.ts') || entry.endsWith('.tsx')) &&
      !entry.endsWith('.test.ts') &&
      !entry.endsWith('.test.tsx')
    ) {
      out.push(abs);
    }
  }
}

const PRODUCTION_SOURCE_FILES: readonly string[] = (() => {
  const out: string[] = [];
  collectProductionSourceFiles(resolve(REPO_ROOT, 'src'), out);
  return out;
})();

